package domain;

public class Pyls {
	String pyid;
	String pyname;
	String pypass;
	public String getPyid() {
		return pyid;
	}
	public void setPyid(String pyid) {
		this.pyid = pyid;
	}
	public String getPyname() {
		return pyname;
	}
	public void setPyname(String pyname) {
		this.pyname = pyname;
	}
	public String getPypass() {
		return pypass;
	}
	public void setPypass(String pypass) {
		this.pypass = pypass;
	}
	
}
